package Praktikum_A;
public class Buku {
    String judul, pengarang;
    int tahunTerbit, jumlahHalaman;
    double harga;

    Buku(String judul, String pengarang, int tahunTerbit, int jumlahHalaman, double harga) {
        this.judul = judul;
        this.pengarang = pengarang;
        this.tahunTerbit = tahunTerbit;
        this.jumlahHalaman = jumlahHalaman;
        this.harga = harga;
    }
}
